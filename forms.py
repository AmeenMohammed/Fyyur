#genres validation method
def genreValidation(genres):
    def _validate_genre(form, field):
        error = False

        for value in field.data:
            if value not in genres:
                error = True

        if error:
            raise ValidationError('Not an option!')

    return _validate_genre

#genres options enums
class Genres(Enum):
    alternative = 'Alternative'
    blues = 'Blues'
    classical = 'Classical'
    country = 'Country'
    elect = 'Electronic'
    folk = 'Folk'
    funk = 'Funk'
    hipHop = 'Hip-Hop'
    heavyMetal = 'Heavy Metal'
    instru = 'Instrumental'
    jazz = 'Jazz'
    musicalTheatre = 'Musical Theatre'
    pop = 'Pop'
    punk = 'Punk'
    rb = 'R&B'
    reggae = 'Reggae'
    rockNroll = 'Rock n Roll'
    soul = 'Soul'
    other = 'Other'
    
    @classmethod
    def elements(cls):
        return [(element.value, element.value) for element in cls]

